-- Ce script SQL crée une nouvelle base de données nommée hbtn_0c_0 si elle n'existe pas déjà
-- La clause IF NOT EXISTS évite de générer une erreur si la base de données existe déjà
-- Cette commande est utile pour créer une base de données de manière sécurisée sans risquer d'écraser une base existante
CREATE DATABASE IF NOT EXISTS hbtn_0c_0;
